<section class="media-section"id="townsquare" class="jumbotron">
  <div class="text-center">
    <h1> Town Square | Trg Fra Andrije Kacica Miosica </h1>
    <p class="text-center">
      St. Mark's church is a baroque church situated at the top of the Kacic square in Makarska. In front of the church the statue of Andrija Kacic Miosic is located. It was created by Ivan Rendic, and it was put there in the year 1890. Behind the church there
      is an old school, which was built in the 19th century in neorenessance style. To the right of the church there is a water fountain, called Mletacka Cesma, which was built in 1775., when Makarska got drinking water which has inscriptions forbidding
      washing clothes and animal drinking.
    </p>
