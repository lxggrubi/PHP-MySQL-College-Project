<section class="media-section"id="malacological" class="jumbotron">
  <div class="text-center">
    <h1> Malacological Museum </h1>
    <p class="text-center">
      In the Franciscan monastery od Saint Mary, the Malacological Museum is situated. It was founded by a monk Friar Jure Radic and his associates. The museum was created with painstaking attention to scientific detail. It was founded to celebrate the malacology,
      the branch of zoology that studies molluscs. Friar Radic collected over 3000 shells and there is also a collection of invertebrae fossils. The collection has many specimens from around the world, as well some specimens from the Adriatic. There is
      also some flora of the Biokovo mountains and Adriatic region.
    </p>
