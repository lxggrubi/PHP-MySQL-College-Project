<section class="media-section"id="stmark" class="jumbotron">
  <div class="text-center">
    <h1> Saint Mark's Church </h1>
    <p class="text-center">
      St. Mark's church is a baroque church situated at the top of the Kacic square in Makarska. The build of the church started input started in the year 1700., but the church was never truly finished. On the right side to the entrance of the church, the bones
      of St. Clement are kept, who is the protector of Makarska. The bones were transported from Roman catacombs in 1725. The church was badly damaged in 1962., when an earthquake hit. When the repairs were made, the main altar was moved to a adjacent
      chapel. The altar is a beautiful masterpiece by Venetian artists. In the aftermath of the earthquake, the organs were damaged, and in 1970 were replaced.
    </p>
