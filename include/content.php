<section class="media-section"id="peka" class="jumbotron">
  <div class="text-center">
    <h1> Peka </h1>
    <p class="text-center">
      Peka, is an baking style that uses a round, deep pan which is filled with meat, potatoes and various other additions.
      It is then covered with a tall, metal lid and covered with embers and left to cook.
      Peka has an authentic Dalmatian taste, and it is mostly prepared on Sundays, when people have time to prepare it,
      because it is not an easy task to do.
    </p>
