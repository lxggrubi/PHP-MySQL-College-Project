<section class="media-section"id="baked" class="jumbotron">
  <div class="text-center">
    <h1> Baked Lamb </h1>
    <p class="text-center">
      Baked lamb, or as said in Dalmatia, janjetina, is a very important part of Dalmatia's character. It is a whole lamb covered in salt,
      stuck on a metal pole, and placed over a pit filled with embers, and baked for a few hours. It is mainly prepared for celebrations
      and holidays, and can feed a lot of people. It is usually served with potatoes and onions.
    </p>
