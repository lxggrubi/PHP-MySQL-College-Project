<section class="media-section"id="history" class="jumbotron">
  <div class="text-center">
    <h1> History </h1>
    <p class="text-center">
      The history of Makarska dates back to the 4th century. In a document by the Salona Council, a city of Mucrum is mentioned. In the itinerary of the Ravenat, in 7th century, it is mentioned as Aronia id est Mucru.
      In the middle ages, there was a county of Mocro that was famous by its naval might, and it was a part of the Neretva dukedom. On September 17th, 887 AD, a Venetian doge by the name of Petar Kandijan was defeated in a large naval battle in front of Mokro.
      The first time the current name, Makarska, is mentioned is around the year of 1502. When the Turks occupied, Makarska was a part of the Primorje province, and had three keeps to guard it. In 1684, Makarska became part of the Venetian republic, and during
      the Venetian hundred-year rule, the city's foundations were built.
      During WWII, Makarska port served as HQ for Central Adriatic Naval Command. Later, it was moved to Split.
    </p>
