<section class="media-section"id="vepric" class="jumbotron">
  <div class="text-center">
    <h1> Vepric Shrine </h1>
    <p class="text-center">
      On the entrance of Makarska, the Vepric shrine is located. It is situated in a natural setting, at the bottom of the hill. This holy place was founded in 1908 by Juraj Caric, a bishop who is buried in Vepric. The Lourdes sanctuary is similar to Vepric
      sanctuary because of the natural cave and the landscape. The shrine consists of the statue of Our Lady, a chapel, a open-air altar, and stations of the Way of the Cross.
    </p>
