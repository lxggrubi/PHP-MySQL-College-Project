<section class="media-section"id="biokovo" class="jumbotron">
  <div class="text-center">
    <h1> Biokovo </h1>
    <p class="text-center">
      Overlooking Makarska, the Biokovo mountain is situated. It is the second highest mountain range in Criatia. The highest point of Biokovo is Sveti Jure, which stands at 1762 meters high. Approximately 200 km square is a protected nature park Biokovo with
      around 1500 plants and animal species, and some are even endemic. Biokovo is a popular hiking destination, and there is Biokovo Nature Park center in downtown Makarska to aid hikers.
    </p>
