<section class="media-section"id="makarana" class="jumbotron">
  <div class="text-center">
    <h1> Makarana Cake  </h1>
    <p class="text-center">
      Makarana cake is a Croatian cultural dish, and it is on the Croatian Republic's Ministry of Culture's list of protected cultural heritage.
      It is a cake of dukes from Makarska, with almonds and maraschino.
      On the first day, you prepare the biscuit dough, and than it is kept in the refridgerator over the night. After that, you mix the ingredients
      to create the filling. Then you fill the dough with the mix and bake it.
    </p>
