    <h1> Makarska Town Museum </h1>
    <p class="text-center">
      The town museum of Makarska is a great place to go if you want to learn about the city history and culture. In the museum, you can find prehistorical artefacts, weapons from 16th century, and modern period artefacts. The Tonoli Palace, in which the museum
