<section class="media-section"id="fishcrab" class="jumbotron">
  <div class="text-center">
    <h1> Museum of Fish and Crabs </h1>
    <p class="text-center">
      Another museum in Makarska relating to the underwater life is the museum of fish and crabs. It celebrates the fish and crustaceans found exclusively in the Adriatic, mostly from the Makarska area. It houses more than 250 species of fish and crabs, and
      most of them are caught by local Makarska fishermen. There is a fish that has not been caught in Makarska area since 1980s, the sturgeon fish, which is two meters in length and weighs more than 20kg. Also, the museum houses various rare fish, large
      pen shells, sea turtles, bull sharks etc. The museum tells us the story of local fishermen and their adventures.
    </p>
